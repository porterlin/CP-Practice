void solve() {
    int n;
    cin >> n;

    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    vector<int> idx;
    i64 ans = 0;
    for (int i = 0; i < n; i++) {
        if (i + 1 > a[i]) {
            ans += lower_bound(idx.begin(), idx.end(), a[i]) - idx.begin();
            idx.push_back(i + 1);
        }
    }

    cout << ans << "\n";
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}